package com.springboot.healthcare.dto;

import java.util.UUID;

public class UserKafkaEvent {
    private UUID userId;
    private String username;
    private String email;
    private String role;
    private String eventType = "USER_CREATED"; // For future expansion to other event types

    // Default constructor required for Jackson
    public UserKafkaEvent() {
    }

    public UserKafkaEvent(UUID userId, String username, String email, String role) {
        this.userId = userId;
        this.username = username;
        this.email = email;
        this.role = role;
    }

    // Getters and setters
    public UUID getUserId() {
        return userId;
    }

    public void setUserId(UUID userId) {
        this.userId = userId;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getEventType() {
        return eventType;
    }

    public void setEventType(String eventType) {
        this.eventType = eventType;
    }

    @Override
    public String toString() {
        return "UserKafkaEvent{" +
                "userId=" + userId +
                ", username='" + username + '\'' +
                ", email='" + email + '\'' +
                ", role='" + role + '\'' +
                ", eventType='" + eventType + '\'' +
                '}';
    }
}